#include "libft.h"

void		ft_tab_clr(char ***tab)
{
	char	**t;
	int		i;

	t = *tab;
	i = 0;
	while (t[i])
	{
		ft_strdel(&t[i]);
		i++;
	}
	free(t);
	t = NULL;
}
