#include "my_mlx.h"

t_rgb	rgb_mult(t_rgb a, t_rgb b)
{
	t_rgb ret;

	ret.r = a.r * b.r;
	ret.g = a.g * b.g;
	ret.b = a.b * b.b;
	return (ret);
}
